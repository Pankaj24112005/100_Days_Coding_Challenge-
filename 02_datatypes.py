# Python Data Types - Day 2
# Datatypes: A data type defines the type of value a variable can store and the operations that can be performed on that value.

# 1. Integer (int) - Stores whole numbers
a = 10

# 2. Float (float) - Stores decimal numbers
b = 3.14

# 3. Complex (complex) - Stores numbers with real and imaginary parts
c = 2 + 3j

# 4. String (str) - Stores text or characters
d = "Hello Python"

# 5. Boolean (bool) - Stores True or False values
e = True

# 6. List (list) - Ordered and mutable collection of items
f = [1, 2, 3, 4]

# 7. Tuple (tuple) - Ordered and immutable collection of items
g = (10, 20, 30)

# 8. Set (set) - Unordered collection of unique items
h = {1, 2, 3}

# 9. Dictionary (dict) - Stores data in key-value pairs
i = {"name": "Pankaj", "course": "Python"}

# 10. NoneType (None) - Represents absence of a value
j = None

# 11. Bytes (bytes) - Immutable sequence of bytes
k = b"Python"

# 12. Bytearray (bytearray) - Mutable sequence of bytes
l = bytearray([65, 66, 67])

# Printing values and their data types
print(type(a), a)
print(type(b), b)
print(type(c), c)
print(type(d), d)
print(type(e), e)
print(type(f), f)
print(type(g), g)
print(type(h), h)
print(type(i), i)
print(type(j), j)
print(type(k), k)
print(type(l), l)



