# Python Variables - Day 2
# Variables: A variable is a named memory location used to store data that can be changed during program execution.

# 1. Variable - A container used to store a value
x = 10

# 2. Reassigning Variable - Value of a variable can be changed
x = 20

# 3. Multiple Assignment - Assigning multiple values in one line
a, b, c = 1, 2, 3

# 4. Same Value to Multiple Variables - One value to many variables
p = q = r = 100

# 5. Variable with String Value - Stores text data
name = "Pankaj"

# 6. Variable with Float Value - Stores decimal numbers
price = 99.99

# 7. Boolean Variable - Stores True or False
is_active = True

# 8. Dynamic Typing - Variable can change its data type
data = 10
data = "Python"

# 9. Variable using Expression - Stores result of an expression
result = 10 + 5

# 10. Variable with None - Represents no value
value = None

# Printing values and their types
print(x)
print(a, b, c)
print(p, q, r)
print(name)
print(price)
print(is_active)
print(data)
print(result)
print(value)
